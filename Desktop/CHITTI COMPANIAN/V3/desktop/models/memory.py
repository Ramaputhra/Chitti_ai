@dataclass
class MemorySnapshot:
    """
    A point-in-time read-only projection of memory state.
    Built by MemoryRuntime.snapshot() and consumed by PlannerRuntime.
    Provides the planner with its full context: recent interactions,
    active working memory, known facts, episodes, and graph relationships.

    Rule 85: Context is a read-only projection of system state.
    Rule 36: Memory reads are cheap, writes are explicit.
    """
    session_id: str
    workflow_id: str = "global"
    session_state: SessionState = SessionState.ACTIVE
    recent_interactions: List[InteractionRecord] = field(default_factory=list)
    working_memory: List[WorkingMemoryEntry] = field(default_factory=list)
    facts: List[FactRecord] = field(default_factory=list)
    episodes: List[EpisodeRecord] = field(default_factory=list)
    relationships: List[RelationshipRecord] = field(default_factory=list)
    episode_hints: List[MemoryEpisodeHint] = field(default_factory=list)
    # Legacy compatibility fields (used by planner_contracts.py)
    records: List[Any] = field(default_factory=list)
