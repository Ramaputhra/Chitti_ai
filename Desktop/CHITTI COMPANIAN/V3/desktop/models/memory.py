@dataclass
class ReplayStatistics:
    """
    COG-31F: Operational metadata tracking long-term execution performance of an Episode.
    Stored in MemoryRuntime metadata tables for analytics and retrieval diagnostics.
    Does NOT affect execution or planner decisions directly.
    """
    replay_count: int = 0
    success_count: int = 0
    failure_count: int = 0
    average_latency_ms: float = 0.0
    last_used_timestamp: float = 0.0
    last_success_timestamp: Optional[float] = None

    @property
    def success_rate(self) -> float:
        total = self.success_count + self.failure_count
        return (self.success_count / total) if total > 0 else 0.0

