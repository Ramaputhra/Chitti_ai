@dataclass
class Episode:
    """
    Phase 3 Contract #6: Ultimate, immutable record coupling Intent, Execution, and Verification.
    Acts as the basic unit of CHITTI's experience.
    """
    episode_id: str
    intent: Any
    execution_plan: Any
    execution_trace: Any
    verification_result: Any
    fingerprint: Any
    timestamp: float
    version: str = "1.0"
    episode_score: Optional[EpisodeScore] = None
    episode_quality: Optional[EpisodeQuality] = None

    def to_dict(self) -> Dict[str, Any]:
        plan_dict = None
        if self.execution_plan:
            if hasattr(self.execution_plan, "to_dict"):
                plan_dict = self.execution_plan.to_dict()
            elif isinstance(self.execution_plan, dict):
                plan_dict = self.execution_plan
            else:
                workflows_serialized = []
                for wf in getattr(self.execution_plan, "workflows", []):
                    workflows_serialized.append({
                        "action": getattr(wf, "action", ""),
                        "correlation_id": getattr(wf, "correlation_id", ""),
                        "parameters": getattr(wf, "parameters", {}) or {}
                    })
                plan_dict = {
                    "schema_version": "1.0",
                    "planner_version": "1.0.0-deterministic",
                    "runtime_version": "3.0",
                    "decision_quality": getattr(getattr(self.execution_plan, "decision_quality", None), "value", "CERTAIN"),
                    "workflows": workflows_serialized
                }

        trace_dict = None
        if self.execution_trace:
            if hasattr(self.execution_trace, "to_dict"):
                trace_dict = self.execution_trace.to_dict()
            elif isinstance(self.execution_trace, dict):
                trace_dict = self.execution_trace
            else:
                steps_serialized = []
                for s in getattr(self.execution_trace, "steps", []):
                    steps_serialized.append({
                        "step_id": getattr(s, "step_id", ""),
                        "capability_name": getattr(s, "capability_name", ""),
                        "status": getattr(getattr(s, "status", None), "value", str(getattr(s, "status", ""))),
                        "start_time": getattr(s, "start_time", 0.0),
                        "end_time": getattr(s, "end_time", 0.0),
                        "parameters": getattr(s, "parameters", {}) or {},
                        "output_payload": getattr(s, "output_payload", {}) or {},
                        "error_message": getattr(s, "error_message", None)
                    })
                trace_dict = {
                    "trace_id": getattr(self.execution_trace, "trace_id", ""),
                    "plan_id": getattr(self.execution_trace, "plan_id", ""),
                    "total_duration_ms": getattr(self.execution_trace, "total_duration_ms", 0.0),
                    "overall_status": getattr(getattr(self.execution_trace, "overall_status", None), "value", str(getattr(self.execution_trace, "overall_status", ""))),
                    "version": getattr(self.execution_trace, "version", "1.0"),
                    "steps": steps_serialized
                }

        v_result_dict = None
        if self.verification_result:
            if hasattr(self.verification_result, "to_dict"):
                v_result_dict = self.verification_result.to_dict()
            elif isinstance(self.verification_result, dict):
                v_result_dict = self.verification_result
            else:
                v_result_dict = {
                    "status": getattr(getattr(self.verification_result, "status", None), "value", str(getattr(self.verification_result, "status", ""))),
                    "evidence": getattr(self.verification_result, "evidence", []),
                    "version": getattr(self.verification_result, "version", "1.0")
                }

        fp_dict = None
        if self.fingerprint:
            if hasattr(self.fingerprint, "to_dict"):
                fp_dict = self.fingerprint.to_dict()
            elif isinstance(self.fingerprint, dict):
                fp_dict = self.fingerprint

        return {
            "episode_id": self.episode_id,
            "intent": self.intent if isinstance(self.intent, (dict, str, int, float, list)) else str(self.intent),
            "execution_plan": plan_dict,
            "execution_trace": trace_dict,
            "verification_result": v_result_dict,
            "fingerprint": fp_dict,
            "timestamp": self.timestamp,
            "version": self.version,
            "episode_score": self.episode_score.to_dict() if self.episode_score else None,
            "episode_quality": self.episode_quality.to_dict() if self.episode_quality else None,
        }

    @classmethod
    def from_dict(cls, data: Dict[str, Any]) -> 'Episode':
        if not data:
            return None
        from desktop.models.environment import EnvironmentFingerprint
        
        score_data = data.get("episode_score")
        quality_data = data.get("episode_quality")
        fp_data = data.get("fingerprint")

        return cls(
            episode_id=data.get("episode_id", ""),
            intent=data.get("intent"),
            execution_plan=data.get("execution_plan"),
            execution_trace=data.get("execution_trace"),
            verification_result=data.get("verification_result"),
            fingerprint=EnvironmentFingerprint.from_dict(fp_data) if isinstance(fp_data, dict) else fp_data,
            timestamp=data.get("timestamp", 0.0),
            version=data.get("version", "1.0"),
            episode_score=EpisodeScore.from_dict(score_data) if isinstance(score_data, dict) else None,
            episode_quality=EpisodeQuality.from_dict(quality_data) if isinstance(quality_data, dict) else None,
        )

