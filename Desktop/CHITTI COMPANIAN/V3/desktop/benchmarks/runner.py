import os
import json
from typing import List

from desktop.benchmarks.jobs.benchmark_001_find_file import Benchmark001FindFile
from desktop.benchmarks.benchmark import CognitiveBenchmark

def run_benchmarks():
    benchmarks: List[CognitiveBenchmark] = [
        Benchmark001FindFile()
    ]
    
    results = []
    
    print("--- Starting Cognitive Benchmark Suite ---\n")
    
    for benchmark in benchmarks:
        print(f"Running Benchmark {benchmark.metadata.id}: {benchmark.metadata.name}...")
        
        try:
            # Execute
            result_data = benchmark.execute()
            
            # Assert
            benchmark_result = benchmark.assert_success(result_data)
            results.append((benchmark, benchmark_result))
            
            status = "PASS" if benchmark_result.passed else "FAIL"
            print(f"[{status}] {benchmark.metadata.id} {benchmark.metadata.name} ({benchmark_result.latency_ms:.1f}ms)")
            
        except Exception as e:
            print(f"[ERROR] {benchmark.metadata.id} {benchmark.metadata.name} threw exception: {e}")
            
    # Generate the Markdown Report
    generate_report(results)
    # Generate machine-readable registry/manifest
    generate_registry(benchmarks)
    
def generate_report(results):
    report_lines = [
        "# Cognitive Benchmark Results\n",
        "This file is automatically generated. Do not edit manually.\n\n"
    ]
    
    total = len(results)
    passed = sum(1 for _, res in results if res.passed)
    success_rate = (passed / total * 100) if total > 0 else 0
    
    report_lines.append(f"## Summary\n")
    report_lines.append(f"Total Jobs: {total}\n")
    report_lines.append(f"Success Rate: {success_rate:.1f}%\n\n")
    
    report_lines.append("## Details\n")
    
    for benchmark, res in results:
        status_icon = "✓" if res.passed else "✗"
        status_text = "PASS" if res.passed else "FAIL"
        
        report_lines.append(f"### {status_icon} {benchmark.metadata.id} {benchmark.metadata.name}\n")
        report_lines.append(f"**Status:** {status_text}\n")
        report_lines.append(f"**Goal Status:** {res.goal_status}\n")
        report_lines.append(f"**Latency:** {res.latency_ms:.1f} ms\n")
        report_lines.append(f"**Events Emitted:** {res.events_count}\n")
        if res.extracted_path:
            report_lines.append(f"**Extracted Output:** {res.extracted_path}\n")
        if not res.passed and res.error_message:
            report_lines.append(f"**Error:** {res.error_message}\n")
        report_lines.append("\n---\n\n")
        
    with open("BENCHMARK_RESULTS.md", "w") as f:
        f.writelines(report_lines)
    
    print("\nGenerated BENCHMARK_RESULTS.md")
    
def generate_registry(benchmarks: List[CognitiveBenchmark]):
    registry_data = {
        "benchmarks": []
    }
    for b in benchmarks:
        registry_data["benchmarks"].append({
            "id": b.metadata.id,
            "name": b.metadata.name,
            "layer": b.metadata.layer,
            "deterministic": b.metadata.deterministic,
            "required_capabilities": b.metadata.required_capabilities,
            "description": b.metadata.description
        })
        
    with open("desktop/benchmarks/benchmark_registry.json", "w") as f:
        json.dump(registry_data, f, indent=4)
        
    print("Generated desktop/benchmarks/benchmark_registry.json")

if __name__ == "__main__":
    run_benchmarks()
